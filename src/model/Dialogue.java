package model;

public class Dialogue {
    private String character;
    private String text;

    // Getters
    public String getCharacter() {
        return character;
    }

    public String getText() {
        return text;
    }

    // Setters
    public void setCharacter(String character) {
        this.character = character;
    }

    public void setText(String text) {
        this.text = text;
    }
}
